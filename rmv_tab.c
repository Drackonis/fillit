#include "fillit.h"

char	**rmv_tab(char c, char **tab)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (c < 65 || c > 90)
		return (NULL);
	while (tab[j])
	{
		while (tab[j][i])
		{
			if (tab[j][i] == c)
				tab[j][i] = '.';
			i++;
		}
		j++;
	}
	return (tab);
}

